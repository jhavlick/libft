#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
	{
		str++;
	}
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			j++;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		i *= 10;
		i += *str - 48;
		str++;
	}
	if (!(j % 2))
		return (i);
	return (-i);
}
